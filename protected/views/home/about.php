				<h3>What other treatments can I have for my back and neck pain?</h3>

				<p>Other treatments like yoga, pillate and massage may help to control the pain. Regular exercises like brisk walking, jogging and swimming help to strengthen the back and neck muscles. We do not encourage our patients to have chiropractic treatment. It is generally not harmful. However, there are differences in the philosophy on the causes and the management of spine problems.  </p>


