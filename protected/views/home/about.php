				<h3>Are back pain and neck pain treatable conditions?</h3>

				<p>Back pain and neck pain are treatable conditions. The treatment depends on the cause of the problem. With appropriate treatment and advice, 80% of patients get better within 6 weeks. Operation is rarely required and is usually reserved if other conservative treatments fail to improve the conditions. </p>


				<div class="fcs-line"></div>
				<div class="height-10"></div>
				<h3>When do I need to see a doctor for my back pain and neck pain?</h3>

				<p>You should see your doctor if the back pain does not show any sign of improvement within a month or you feel unwell. Your doctor will ask detailed questions, do physical examination and order some radiological investigations to determine what could be the cause of the problem. Generally the cause is not serious. The doctor may give you medicine to control the pain. It is not advisible to take pain killer for a long term because of its side effects. Less than 5% of back pain and neck pain are caused by something more serious such as tumor, infection, fracture and nerve compression. These conditionsoften require early treatment. </p>


				<div class="fcs-line"></div>
				<div class="height-10"></div>
				<h3>When do you need to have physiotherapy treatment?</h3>

				<p>Most of back pain and neck pain can be treated with physiotherapy. The doctors will decide if physiotherapy treatment is suitable for you. The aims of the physiotherapy treatment are to first control the pain to an acceptable level and then to strengthen the back and neck muscles. Strong muscles stabilize the spine to further reduce the pain from instability and to reduce the rate of ageing in the spine. </p>


				<div class="fcs-line"></div>
				<div class="height-10"></div>
				<h3>What to expect after physiotherapy treatment?</h3>

				<p>On the first physiotherapy visit, the physiotherapist will advise you on how many times and how often you need to have the physiotherapy treatment. Usually the pain improves significantly within a few weeks. If there is evidence of ageing in the spine, the pain may not go away completely. The pain may come back on and off. You may require more frequent treatment to control the pain. </p>


				<div class="fcs-line"></div>
